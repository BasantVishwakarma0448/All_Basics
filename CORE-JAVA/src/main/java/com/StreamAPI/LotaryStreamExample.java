package com.StreamAPI;

public class LotaryStreamExample {
	String no;
	String name;

	public LotaryStreamExample(String no, String name) {
		super();
		this.no = no;
		this.name = name;
	}

	public String getNo() {
		return no;
	}

	public String getName() {
		return name;
	}

	public void setNo(String no) {
		this.no = no;
	}

	public void setName(String name) {
		this.name = name;
	}

}
